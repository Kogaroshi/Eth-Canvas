const intToHex = (value) => {
    let color = value.toString(16);
    if(color.length != 6){
        return ("0".repeat(6 - color.length)) + color;
    }
    return color;
}



const hexToInt = (color) => {
    return parseInt(color, 16);
}



const displayOwned = async () => {
    $("#pixel-list").html();
    for(let p in ownedPixels){
        let id = ownedPixels[p];
        let color = await Pixel.methods.getColor(id).call();
        color = intToHex(color);
        //change int to css color
        let newHTML = `
            <li id="pixel-${id}" class="py-2 owned-pixel">
                Pixel #${id} - Color #${color}   
                <span class="display-color" style="background-color: #${color}">&nbsp;&nbsp;&nbsp;&nbsp;</span>
            </li>
        `;
        $("#pixel-list").append(newHTML);
        $("#pixel-"+id).click(() => {
            changeSelected(id);
        });
    }
    $("#pixel-"+ownedPixels[0]).addClass('selected');
}



