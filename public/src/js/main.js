var pixels = {};
var ownedPixels = undefined;
var buyPrice = undefined;
var changePrice = undefined;
var selectedPixel = 0;
var Pixel = undefined; //contract



$(document).ready(async function () {
    $("body").tooltip({ selector: '[data-toggle=tooltip]' });

    window.web3 = new Web3(window.ethereum);
    await window.ethereum.enable();
    var userAccount = (await web3.eth.getAccounts())[0];

    $("#loggedIn").html(userAccount);

    Pixel = new web3.eth.Contract(pixelABI, pixelAddress);

    //retrive prices
    buyPrice = await Pixel.methods.buyingFee().call();
    changePrice = await Pixel.methods.changingFee().call();



    //retrieve already bought pixels ids -> Then color for each
    var pixelIds = await Pixel.methods.getAllIds().call();
    for(let id of pixelIds){
        let color = await Pixel.methods.getColor(id).call();
        pixels[id] = color;
    }

    //retrive list of owned Pixels
    ownedPixels = await Pixel.methods.getPixelsByOwner(userAccount).call();

    displayGrid();
    displayOwned();
    changeSelected(0);
});



const changeSelected = (id) => {
    $("#pixel-"+selectedPixel).removeClass('selected');
    $(".selected-Pixel").html(id);
    selectedPixel = id;
    $("#pixel-"+id).addClass('selected');
}



$("#pixels-tab").click(() => {
    if(ownedPixels != undefined){
        changeSelected(ownedPixels[0]);
    }
})



const displayGrid = async () => {
    for(let i = 0; i < 1000; i++){
        let row = `<div class="row" id="row-${i}"></div>`;
        $("#canvas-grid").append(row);
        for(let j = 0; j < 1000; j++){
            let color;
            if(pixels[i+j]){
                color = pixels[i+j];
            }
            else {
                color = intToHex(randomValue());
            }
            let pix = `<div id="pixel-${i+j}" class="pixel" style="background-color: #${color};"></div>`;
            $("#row-"+i).append(pix)
            $("#pixel-"+(i+j)).click(() => {
                changeSelected(i+j);
                //voir fct pour bouton acheter activer ou pas
            });
        }
    }
}



